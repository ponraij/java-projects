public class varibles {
    public static void main(String[] args) {

        // A variable is  a location in a memory to hold (store data) the data.
        // Data types specifies the type of data that can be stored inside the variable.

        // Java is a statically-typed language.
        // This means that all variables must be declared before they can be used.

        boolean booleanValue = true;
        System.out.println(booleanValue);

        int binaryNumber = 0b10010;
        System.out.println(binaryNumber);
        int octalNumber = 023;
        System.out.println(octalNumber);
        int decimalNumber = 23;
        System.out.println(decimalNumber);
        int hexaDecimalNumber = 0x19;
        System.out.println(hexaDecimalNumber);

        float floatNumber = 3.4F;
        System.out.println(floatNumber);

        double doubleNumber = 3.44e2;
        System.out.println(doubleNumber);

        char charLetter = 'A';
        System.out.println(charLetter);

        String name = "ponraj Nonara";
        System.out.println(name);


    }
}